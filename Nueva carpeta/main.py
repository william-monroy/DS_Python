# Variables independientes
a, b, c = 'string', 123, 3.1416
print(type(a), a)
print(type(b), b)
print(type(c), c)

# A partir de una tupla
mi_tupla = ("hola mundo", 2011)
texto, year = mi_tupla
print(type(texto), texto)
print(type(year), year)

# A partir de una lista
mi_lista = ['Peru', 'Arequipa']
pais, provincia = mi_lista
print(type(pais), pais)
print(type(provincia), provincia)

